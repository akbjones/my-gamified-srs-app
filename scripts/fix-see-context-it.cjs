let content = fs.readFileSync(DICT_PATH, 'utf8');

let fixedCount = 0;
let missedWords = [];

// For each translation, do a targeted replacement
for (const [word, translation] of Object.entries(TRANSLATIONS)) {
  // Escape special regex characters in the word
  const escaped = word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

  // Match the exact dictionary entry pattern
  const regex = new RegExp(
    `("${escaped}": \\{ en: )"see context"`,
    'g'
  );

  const before = content;
  content = content.replace(regex, `$1"${translation}"`);

  if (content !== before) {
    fixedCount++;
  }
}

// Check how many "see context" remain
const remaining = (content.match(/"see context"/g) || []).length;

fs.writeFileSync(DICT_PATH, content, 'utf8');

console.log(`Fixed: ${fixedCount} entries`);
console.log(`Remaining "see context": ${remaining}`);

if (remaining > 0) {
  // Find remaining words
  const lines = content.split('\n');
  for (const line of lines) {
    const m = line.match(/"([^"]+)": \{ en: "see context"/);
    if (m) missedWords.push(m[1]);
  }
  console.log(`\nMissed words (${missedWords.length}):`);
  missedWords.forEach(w => console.log(`  "${w}"`));
}
