const cards = [];
let id = 1;

const tagSets = [
  ['general', 'travel'], ['general', 'work'], ['general', 'family'],
  ['general', 'travel', 'work'], ['general', 'travel', 'family'],
  ['general', 'work', 'family'], ['general'],
];

for (const node of NODES) {
  for (let i = 0; i < CARDS_PER_NODE; i++) {
    const templateIdx = i % node.templates.length;
    const [target, english] = node.templates[templateIdx];
    const tags = tagSets[i % tagSets.length];
    const card = {
      id: id,
      target: target,
      english: english,
      audio: `nl-${id}.mp3`,
      tags: tags,
      grammarNode: node.id,
    };
    if (i % 4 === 0 && node.tips.length > 0) {
      card.grammar = node.tips[Math.floor(i / 4) % node.tips.length];
    }
    cards.push(card);
    id++;
  }
}

