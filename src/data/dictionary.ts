// Spanish → English dictionary with IPA phonetics
// Covers the most common ~500 words to start; will be expanded with the full deck

export interface DictEntry {
  en: string;   // English translation
  ipa: string;  // IPA pronunciation
  pos?: string; // Part of speech: n, v, adj, adv, prep, conj, det, pron
}

// Lookup helper: strips punctuation, lowercases, tries base forms
export function lookupWord(word: string): DictEntry | null {
  const clean = word.toLowerCase().replace(/[¿¡.,!?;:"""''()—–\-]/g, '').trim();
  if (!clean) return null;

  // Direct match
  if (dictionary[clean]) return dictionary[clean];

  // Try common verb endings → infinitive approximations
  // This is a simple heuristic; real lemmatization would be better
  const verbEndings = [
    { suffix: 'ando', replace: 'ar' },
    { suffix: 'iendo', replace: 'er' },
    { suffix: 'ando', replace: 'ar' },
    { suffix: 'amos', replace: 'ar' },
    { suffix: 'emos', replace: 'er' },
    { suffix: 'imos', replace: 'ir' },
    { suffix: 'an', replace: 'ar' },
    { suffix: 'en', replace: 'er' },
    { suffix: 'as', replace: 'ar' },
    { suffix: 'es', replace: 'er' },
    { suffix: 'ó', replace: 'ar' },
    { suffix: 'é', replace: 'ar' },
    { suffix: 'aba', replace: 'ar' },
    { suffix: 'ía', replace: 'er' },
  ];

  for (const { suffix, replace } of verbEndings) {
    if (clean.endsWith(suffix)) {
      const stem = clean.slice(0, -suffix.length) + replace;
      if (dictionary[stem]) return dictionary[stem];
    }
  }

  // Try removing plural -s / -es
  if (clean.endsWith('es') && dictionary[clean.slice(0, -2)]) return dictionary[clean.slice(0, -2)];
  if (clean.endsWith('s') && dictionary[clean.slice(0, -1)]) return dictionary[clean.slice(0, -1)];

  // Try removing -a/-o gender suffix
  if (clean.endsWith('a') && dictionary[clean.slice(0, -1) + 'o']) return dictionary[clean.slice(0, -1) + 'o'];
  if (clean.endsWith('o') && dictionary[clean.slice(0, -1) + 'a']) return dictionary[clean.slice(0, -1) + 'a'];

  return null;
}

