// ── Main lookup function ──────────────────────────────────────
export function lookupWord(raw: string): DictEntry | null {
  // 1. Clean input: lowercase, strip punctuation
  let clean = raw.toLowerCase().replace(/[¿¡.,!?;:"""\u2018\u2019()—–«»\d/]/g, '').trim();
  if (!clean || clean.length < 1) return null;

  // 2. Direct dictionary lookup
  if (dictionary[clean]) return dictionary[clean];

  // 3. Irregular verb form → infinitive
  if (IRREGULAR_MAP[clean]) {
    const inf = IRREGULAR_MAP[clean];
    if (dictionary[inf]) return dictionary[inf];
  }

  // 4. Diminutive reversal: strip -etje, -pje, -kje, -tje, -je
  for (const suf of ['etje', 'pje', 'kje', 'tje', 'je']) {
    if (clean.endsWith(suf) && clean.length > suf.length + 2) {
      const base = clean.slice(0, -suf.length);
      if (dictionary[base]) return dictionary[base];
      // Try with doubled last consonant removed
      if (base.length > 1 && base[base.length - 1] === base[base.length - 2]) {
        const shorter = base.slice(0, -1);
        if (dictionary[shorter]) return dictionary[shorter];
      }
    }
  }

  // 5. Plural stripping: -eren, -en, -'s, -s
  if (clean.endsWith('eren') && clean.length > 6) {
    const base = clean.slice(0, -4);
    if (dictionary[base]) return dictionary[base];
    // kind→kinderen
    if (dictionary[base + 'e']) return dictionary[base + 'e'];
  }
  if (clean.endsWith('en') && clean.length > 3) {
    const base = clean.slice(0, -2);
    if (dictionary[base]) return dictionary[base];
    // Double vowel restoration: straten→straat
    if (base.length > 1) {
      const lastChar = base[base.length - 1];
      const doubled = base + lastChar;
      if (dictionary[doubled]) return dictionary[doubled];
    }
  }
  if (clean.endsWith("'s")) {
    const base = clean.slice(0, -2);
    if (dictionary[base]) return dictionary[base];
  }
  if (clean.endsWith('s') && clean.length > 2) {
    const base = clean.slice(0, -1);
    if (dictionary[base]) return dictionary[base];
  }

  // 6. Past participle: ge-...-d/t → strip, find infinitive (-en)
  if (clean.startsWith('ge') && clean.length > 4) {
    let stem = clean.slice(2);
    if (stem.endsWith('d') || stem.endsWith('t')) {
      stem = stem.slice(0, -1);
      const inf = stem + 'en';
      if (dictionary[inf]) return dictionary[inf];
      if (dictionary[stem]) return dictionary[stem];
    }
    // ge-...-en (strong past participle)
    if (stem.endsWith('en')) {
      if (dictionary[stem]) return dictionary[stem]; // e.g. gevallen→vallen
      const base = stem.slice(0, -2);
      if (dictionary[base + 'en']) return dictionary[base + 'en'];
    }
    // Direct ge- prefix removal
    if (dictionary[stem]) return dictionary[stem];
    if (dictionary[stem + 'en']) return dictionary[stem + 'en'];
  }

  // 7. Regular verb form reversal
  // Present: stem+t → infinitive (stem+en)
  if (clean.endsWith('t') && clean.length > 3) {
    const stem = clean.slice(0, -1);
    const inf = stem + 'en';
    if (dictionary[inf]) return dictionary[inf];
  }
  // Past tense: stem+te/de, stem+ten/den
  for (const pastSuf of ['ten', 'den', 'te', 'de']) {
    if (clean.endsWith(pastSuf) && clean.length > pastSuf.length + 2) {
      const stem = clean.slice(0, -pastSuf.length);
      const inf = stem + 'en';
      if (dictionary[inf]) return dictionary[inf];
    }
  }

  // 8. Adjective form stripping: -e suffix (grote→groot, mooie→mooi)
  if (clean.endsWith('e') && clean.length > 3) {
    const base = clean.slice(0, -1);
    if (dictionary[base]) return dictionary[base];
    // Handle mooie→mooi (strip trailing vowel doubling)
    if (base.endsWith('i')) {
      const shorter = base.slice(0, -1);
      if (dictionary[shorter + 'i']) return dictionary[shorter + 'i'];
    }
  }

  // 9. Adverb/comparative: -er, -st, -ste suffixes
  for (const compSuf of ['ste', 'ster', 'st', 'er']) {
    if (clean.endsWith(compSuf) && clean.length > compSuf.length + 2) {
      const base = clean.slice(0, -compSuf.length);
      if (dictionary[base]) return dictionary[base];
      // Double consonant: dikker→dik
      if (base.length > 1 && base[base.length - 1] === base[base.length - 2]) {
        const shorter = base.slice(0, -1);
        if (dictionary[shorter]) return dictionary[shorter];
      }
    }
  }

  // 10. Compound word splitting: try from right
  for (let i = 3; i < clean.length - 2; i++) {
    const right = clean.slice(i);
    if (dictionary[right]) return dictionary[right];
    // Try with linking -s-, -en-
    if (right.startsWith('s') && dictionary[right.slice(1)]) return dictionary[right.slice(1)];
    if (right.startsWith('en') && dictionary[right.slice(2)]) return dictionary[right.slice(2)];
  }

  return null;
}

